"""SQLAlchemy declarative models — JobRadar persistence layer (v1).

Why this file exists
====================
The exercise on this branch is *schema + migration files only*. The FastAPI
routes (``backend/routes/*.py``) still use their in-memory ``_FOO_DB`` dicts
for the demo and they have to keep working without change — that's the
explicit user requirement.

This module therefore defines:

1. The **declarative ``Base``** that Alembic reads to autogenerate
   migration scripts. Any new column added here will be picked up by
   ``alembic revision --autogenerate`` even though the routes never query
   the database today.
2. The full set of **Postgres-native enum types** mirroring the
   ``Literal[...]`` Pydantic enums in :mod:`backend.routes.companies`,
   :mod:`backend.routes.jobs`, :mod:`backend.routes.applications`,
   :mod:`backend.routes.qa_bank`, and :mod:`backend.routes.outreach`.
   Expanding any of those enums requires updating both sides together.
3. Sixteen tables — see ``docs/02-version2-add-postgres.md`` for the full
   design rationale. Each table's columns mirror the corresponding Pydantic
   model 1-for-1, so the eventual route rewrite is mechanical (replace
   ``_FOO_DB[id]`` with ``(await session.execute(select(Foo).where(...))).scalar_one_or_none()``).

Design rules baked into every model
===================================

* **UUID primary keys** generated by Postgres (``gen_random_uuid`` from
  ``pgcrypto``; built-in since PG 13). The migration enables the
  ``pgcrypto`` extension explicitly so Supabase projects that arrive at
  PG 16 without it enabled by default still work.
* **TIMESTAMPTZ** for every timestamp. Existing routes emit ISO-8601
  strings; the new schema stores real ``datetime`` objects and the route
  rewrite can do the conversion at the Pydantic boundary.
* **TEXT[] for tag-shaped arrays** (``tags``, ``hiring_signals``,
  ``target_roles``). Postgres array literals (``ANY(tags)``,
  ``@> ARRAY['python']``) are dramatically cheaper to index with GIN
  than JSONB for the simple-string-tuple use case these enums have.
* **Supabase Storage reference (not ``bytea``) for resume bytes.** 10 MiB
  resumes bloat Postgres pages and tank the buffer cache; the schema
  stores a ``storage_path`` and the route rewrite will upload to Supabase
  Storage. The ``resumes`` table now also has a partial-unique index on
  ``is_default`` to enforce the "single default resume" invariant that
  :func:`routes.resumes.patch_resume` enforces in Python today.
* **Singleton tables** (``preferences``, ``pipeline_status``) use
  ``CHECK (id = 1)`` to enforce single-row at the database level —
  cheaper and more reliable than application-level guards.
* **No soft-delete.** JobRadar expresses lifecycle through explicit
  ``status`` enums (``dismissed``, ``rejected``, ``ghosted``); a global
  ``deleted_at`` would litter every query with a soft-delete predicate
  for zero benefit.
"""
