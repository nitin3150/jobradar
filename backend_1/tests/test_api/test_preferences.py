"""Tests for the /settings (preferences) endpoint schema + singleton seed."""
from types import SimpleNamespace

import pytest
from pydantic import ValidationError

from app.api.settings import get_or_seed_preferences
from app.models.preferences import (
    DEFAULT_JOB_FIT_THRESHOLD,
    DEFAULT_REVIEW_WINDOW_HOURS,
    DEFAULT_SEND_FOLLOWUP_EMAILS,
    DEFAULT_TARGET_ROLES,
    Preferences,
)
from app.schemas.preferences import PreferencesResponse, PreferencesUpdate


# --- PreferencesUpdate schema -------------------------------------------------


def test_update_rejects_review_window_below_min() -> None:
    with pytest.raises(ValidationError):
        PreferencesUpdate(review_window_hours=0.1)


def test_update_rejects_review_window_above_max() -> None:
    with pytest.raises(ValidationError):
        PreferencesUpdate(review_window_hours=49)


def test_update_rejects_fit_below_zero() -> None:
    with pytest.raises(ValidationError):
        PreferencesUpdate(job_fit_threshold=-0.01)


def test_update_rejects_fit_above_one() -> None:
    with pytest.raises(ValidationError):
        PreferencesUpdate(job_fit_threshold=1.5)


def test_update_accepts_boundaries() -> None:
    PreferencesUpdate(review_window_hours=0.5, job_fit_threshold=0.0)
    PreferencesUpdate(review_window_hours=48, job_fit_threshold=1.0)


def test_update_exclude_unset_keeps_omitted_fields() -> None:
    """`exclude_unset=True` is the load-bearing PATCH idiom: unmentioned
    keys must not collapse to Pydantic defaults."""
    payload = PreferencesUpdate(review_window_hours=3.0)
    dump = payload.model_dump(exclude_unset=True)
    assert dump == {"review_window_hours": 3.0}


def test_update_exclude_unset_with_multiple_fields() -> None:
    payload = PreferencesUpdate(
        review_window_hours=3.0,
        send_followup_emails=False,
    )
    dump = payload.model_dump(exclude_unset=True)
    assert dump == {"review_window_hours": 3.0, "send_followup_emails": False}


# --- Response schema ---------------------------------------------------------


def test_response_round_trips() -> None:
    response = PreferencesResponse(
        target_roles=["AI Engineer", "Backend"],
        review_window_hours=DEFAULT_REVIEW_WINDOW_HOURS,
        job_fit_threshold=DEFAULT_JOB_FIT_THRESHOLD,
        send_followup_emails=DEFAULT_SEND_FOLLOWUP_EMAILS,
        updated_at="2026-01-01T00:00:00Z",
    )
    dumped = response.model_dump()
    assert dumped["target_roles"] == ["AI Engineer", "Backend"]
    assert dumped["review_window_hours"] == DEFAULT_REVIEW_WINDOW_HOURS


