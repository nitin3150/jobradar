class AtsDiscoveredOrg(Base):
    """Replaces the per-board JSON files ``backend/data/<board>_missing_orgs.json``.

    Tracks the consecutive-failure count for each (board, slug) so the
    :func:`MISSING_THRESHOLD = 3` benching logic in
    :mod:`backend.pipeline.nodes.jobs_boards.runner` can be expressed
    in a single ``WHERE status = 'active'`` query.
    """

    __tablename__ = "ats_discovered_orgs"

    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        primary_key=True,
        server_default=text("gen_random_uuid()"),
    )
    board: Mapped[str] = mapped_column(ats_board_t, nullable=False)
    slug: Mapped[str] = mapped_column(Text, nullable=False)
    company_id: Mapped[uuid.UUID | None] = mapped_column(
        ForeignKey("companies.id", ondelete="SET NULL"),
        nullable=True,
    )
    status: Mapped[str] = mapped_column(
        ats_org_status_t, nullable=False, server_default="active"
    )
    consecutive_failures: Mapped[int] = mapped_column(
        Integer, nullable=False, server_default="0"
    )
    last_checked_at: Mapped[datetime | None] = mapped_column(
        DateTime(timezone=True), nullable=True
    )
    first_seen_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        nullable=False,
        default=_utcnow,
        server_default=text("now()"),
    )

    __table_args__ = (
        # (board, slug) is the dedupe key — same org never appears twice
        # on a board. The boards runner does INSERT ON CONFLICT here.
        UniqueConstraint("board", "slug", name="uq_ats_orgs_board_slug"),
        Index("idx_ats_orgs_status", "status", "consecutive_failures"),
    )


