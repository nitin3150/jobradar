class Company(Base):
    """The user-facing CRM spine.

    Every LangGraph scanner ultimately promotes a raw ``raw_scrapes_*``
    row into this table (out of scope for v1 schema work). The Pydantic
    model :class:`backend.routes.companies.Company` mirrors these columns
    1-for-1. ``external_id`` is the dedupe key join with the ATS — for
    Ashby/Lever/Greenhouse it's the slug, for GitHub it's ``owner/repo``,
    for NGO listings it's the source URL.
    """

    __tablename__ = "companies"

    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        primary_key=True,
        server_default=text("gen_random_uuid()"),
    )
    title: Mapped[str] = mapped_column(Text, nullable=False)
    organization: Mapped[str] = mapped_column(Text, nullable=False)
    url: Mapped[str | None] = mapped_column(Text, nullable=True)

    # Pydantic fields CompanyCard reads. The literal category is inlined
    # into a per-row CHECK constraint so a typo in application code can't
    # poison the enum until the next migration upgrades the type.
    category: Mapped[str] = mapped_column(
        company_category_t,
        nullable=False,
    )
    score: Mapped[float] = mapped_column(
        Float,
        CheckConstraint("score >= 0.0 AND score <= 1.0", name="companies_score_range"),
        nullable=False,
    )
    source: Mapped[str] = mapped_column(Text, nullable=False)
    tags: Mapped[list[str]] = mapped_column(
        ARRAY(Text),
        nullable=False,
        server_default="{}",
    )
    description: Mapped[str | None] = mapped_column(Text, nullable=True)
    published_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), nullable=False
    )
    location: Mapped[str | None] = mapped_column(Text, nullable=True)
    primary_language: Mapped[str | None] = mapped_column(Text, nullable=True)
    difficulty: Mapped[str | None] = mapped_column(Text, nullable=True)
    stars: Mapped[int | None] = mapped_column(Integer, nullable=True)
    company_summary: Mapped[str | None] = mapped_column(Text, nullable=True)
    hiring_signals: Mapped[list[str]] = mapped_column(
        ARRAY(Text),
        nullable=False,
        server_default="{}",
    )
    status: Mapped[str] = mapped_column(
        company_status_t,
        nullable=False,
        server_default="saved",
    )

    # Dedupe / external join key. Indexed so the boards runner can check
    # ``SELECT ... WHERE external_id = $1`` cheaply.
    external_id: Mapped[str | None] = mapped_column(Text, nullable=True)

    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        nullable=False,
        default=_utcnow,
        server_default=text("now()"),
    )
    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        nullable=False,
        default=_utcnow,
        onupdate=_utcnow,
        server_default=text("now()"),
    )

    __table_args__ = (
        # (category, status, published_at DESC) matches the React
        # CompanyFeed's "filter by category, sort by recency" path.
        Index(
            "idx_companies_feed",
            "category",
            "status",
            text("published_at DESC"),
        ),
        Index("idx_companies_external_id", "external_id"),
    )


