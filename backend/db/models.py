def _utcnow() -> datetime:
    """Naive-free ``datetime.utcnow`` shim.

    Returns timezone-aware UTC datetimes so asyncpg doesn't need to assume
    ``TIMESTAMP`` semantics — Postgres ``TIMESTAMPTZ`` always serialises
    UTC anyway, and ``datetime.utcnow()`` is deprecated in 3.12+.
    """
    return datetime.now(timezone.utc)


