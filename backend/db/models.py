class BoardSeenJob(Base):
    """Replaces the on-disk ``backend/data/seen.json`` file.

    The hourly boards runner currently consults a single-process JSON
    file for "have I delivered this job slug before?". That works for
    one Docker worker, but two concurrent apply-workers would race on
    the read-modify-write cycle. A Postgres table with
    ``INSERT ... ON CONFLICT DO NOTHING`` makes dedup atomic without
    any concurrency gymnastics.
    """

    __tablename__ = "board_seen_jobs"

    # The PK is the dedupe key — typically ``"<board>:<job-id>"``. Text
    # rather than UUID because the boards (Ashby/Lever/Greenhouse)
    # assign their own opaque string IDs and re-encoding them wastes
    # 16 bytes per row.
    job_id_hash: Mapped[str] = mapped_column(Text, primary_key=True)
    board: Mapped[str] = mapped_column(ats_board_t, nullable=False)
    company_id: Mapped[uuid.UUID | None] = mapped_column(
        ForeignKey("companies.id", ondelete="SET NULL"),
        nullable=True,
    )
    first_seen_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        nullable=False,
        default=_utcnow,
        server_default=text("now()"),
    )
    last_seen_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        nullable=False,
        default=_utcnow,
        server_default=text("now()"),
    )
    times_seen: Mapped[int] = mapped_column(
        Integer, nullable=False, server_default="1"
    )

    __table_args__ = (
        Index("idx_board_seen_board_last_seen", "board", text("last_seen_at DESC")),
    )


