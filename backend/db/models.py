class Resume(Base):
    """Resume metadata — bytes live in Supabase Storage, NOT in Postgres.

    Schema rationale: storing 10 MiB blobs in Postgres ``bytea`` blows
    the buffer cache, degrades the WAL stream, and bloats pg_dump
    backups by gigabytes. Supabase Storage is a regular S3-compatible
    bucket and the integration is a one-liner on the route that handles
    ``POST /api/resumes`` (out of scope for v1 schema work).
    """

    __tablename__ = "resumes"

    id: Mapped[uuid.UUID] = mapped_column(
        UUID(as_uuid=True),
        primary_key=True,
        server_default=text("gen_random_uuid()"),
    )
    name: Mapped[str] = mapped_column(Text, nullable=False)
    size_bytes: Mapped[int] = mapped_column(Integer, nullable=False)
    uploaded_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), nullable=False
    )
    tags: Mapped[list[str]] = mapped_column(
        ARRAY(Text), nullable=False, server_default="{}"
    )
    is_default: Mapped[bool] = mapped_column(
        Boolean, nullable=False, server_default=text("false")
    )
    storage_path: Mapped[str] = mapped_column(Text, nullable=False)

    __table_args__ = (
        # Single-default invariant: at most one row may have
        # ``is_default = true``. The Postgres partial unique index
        # enforces this in the engine — ``patch_resume`` no longer
        # has to demote siblings in application logic.
        Index(
            "uq_resumes_single_default",
            "is_default",
            unique=True,
            postgresql_where=text("is_default = true"),
        ),
    )


