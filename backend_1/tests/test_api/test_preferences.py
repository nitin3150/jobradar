# --- Singleton seed (no real DB — fake AsyncSession) -------------------------


@pytest.mark.asyncio
async def test_seed_inserts_default_when_missing() -> None:
    inserted: dict = {}

    class _FakeSession:
        async def get(self, _model, _id):
            return None

        def add(self, obj):
            inserted["obj"] = obj

        async def commit(self):
            inserted["committed"] = True

        async def refresh(self, obj):
            inserted["refreshed"] = True

    sess = _FakeSession()
    result = await get_or_seed_preferences(sess)

    assert inserted.get("committed") is True
    assert inserted.get("refreshed") is True
    assert isinstance(inserted["obj"], Preferences)
    assert result.target_roles == list(DEFAULT_TARGET_ROLES)
    assert result.review_window_hours == DEFAULT_REVIEW_WINDOW_HOURS
    assert result.send_followup_emails is True


@pytest.mark.asyncio
async def test_seed_returns_existing_row_without_writing() -> None:
    fake = SimpleNamespace(
        target_roles=["x"],
        review_window_hours=4.0,
        job_fit_threshold=0.5,
        send_followup_emails=False,
        updated_at="2026-01-01T00:00:00Z",
    )
    seen: dict = {"committed": 0, "added": 0}

    class _FakeSession:
        async def get(self, _model, _id):
            return fake

        def add(self, obj):
            seen["added"] += 1

        async def commit(self):
            seen["committed"] += 1

        async def refresh(self, obj):
            pass

    result = await get_or_seed_preferences(_FakeSession())
    assert result is fake
    assert seen["committed"] == 0
    assert seen["added"] == 0


# --- Route registration via OpenAPI introspection ----------------------------


def test_settings_routes_registered() -> None:
    from app.main import app

    # Use OpenAPI schema (generated by FastAPI from registered routes) instead
    # of walking app.routes directly — Mount objects and Route wrappers don't
    # all expose a `.path` attribute consistently.
    paths = list(app.openapi().get("paths", {}).keys())
    assert "/api/settings" in paths, paths
    # Both verbs should be declared on the new endpoint.
    assert "get" in app.openapi()["paths"]["/api/settings"]
    assert "patch" in app.openapi()["paths"]["/api/settings"]
